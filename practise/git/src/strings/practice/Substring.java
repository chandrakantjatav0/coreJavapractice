package strings.practice;

public class Substring {
	public static void main(String args[]) {
		String c = "Chandrakant Jatav";
		System.out.println("Original String is : " + c);
		{
			System.out.println("Substring Ending from index is 11 : " + c.substring(12));
			{
				System.out.println("Substring Starting from index is 0 to 11 : " + c.substring(0, 11));

			}
		}
	}

}
